namespace Salari
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int nomina;
            int salari = 0;

            Console.WriteLine("Cada dia incrementará el salari");
            while (salari < 15000);
            {
                Convert.ToInt32(Console.ReadLine());
                salari += nomina;
            }
        }
    }
}